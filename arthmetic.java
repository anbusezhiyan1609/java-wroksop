public class arthmetic{
    public static void main(String[] args) {
        int a = 50;
        int b = 20;
        System.out.println("Arithmetic Operators:");
        System.out.println("a + b = " + (a + b));  
        System.out.println("a - b = " + (a - b));  
        System.out.println("a * b = " + (a * b));  
        System.out.println("b / a = " + (b / a));  
        System.out.println("b % a = " + (b % a));   
    }
}
